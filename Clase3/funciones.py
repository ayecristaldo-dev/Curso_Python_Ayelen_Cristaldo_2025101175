'''
  Ejemplo de funciones
'''

def sumar(a, b):
  return a+b

def restar(a, b):
  return a-b

def mult(a, b):
  return a*b

def div(a, b):
  return a/b

def resto(a, b):
  return a%b


n1=3
n2=5
resultado = sumar(n1, n2)
print(f"La suma es: {resultado}")

resultado = restar(n1, n2)
print(f"La resta es: {resultado}")

resultado = mult(n1, n2)
print(f"La mult es: {resultado}")

resultado = div(n1, n2)
print(f"La div es: {resultado}")

resultado = resto(n1, n2)
print(f"El resto es: {resultado}")

